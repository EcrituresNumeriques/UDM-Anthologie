     *     statusCodes={
     *         200="Returned when successful",
     *         401="Returned when the user is not authorized to say hello",
     *         404="Returned when a parameter is not found"
     *     }
     * )
     *
     * @Put("/text/{id}")
     *
     * @param Request      $request
     * @param              $id
     *
     * @return Response
     */
